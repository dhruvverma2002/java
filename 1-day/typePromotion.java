public class typePromotion {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 'b';

        System.out.println(ch1);
        System.out.println(ch2);

        System.out.println((int) ch1);
        System.out.println((int) ch2);

        System.out.println(ch2 - ch1);
    }   
}